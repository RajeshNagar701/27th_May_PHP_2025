    public function store(Request $request)
    {
        $data=new contact;
        $data->name=$request->name;  //$_REQUEST['name'];
        $data->email=$request->email;
        $data->comment=$request->comment;
        $data->save();
        echo "<script>
        alert('Contact Inquiry Success');
        window.location='/contact';
        </script>";
    }

